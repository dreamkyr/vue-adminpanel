<template>
  <div class="modal fade" id="modalFormulaActions" tabindex="-1" role="dialog" aria-labelledby="myModalLabel">
    <div class="modal-dialog" role="document">
      <div class="modal-content">
        <div class="modal-header">
          <button type="button" class="close" data-dismiss="modal" aria-label="Close"><span aria-hidden="true">Ã—</span></button>
          <h4 class="modal-title" id="myModalLabel">Actions</h4>
        </div>
        <div class="modal-header no-padding">
          <form action="#" method="get" class="sidebar-form" @submit="search">
            <div class="input-group">
              <input type="text" name="q" class="form-control" placeholder="Search..." v-model="query">
              <span class="input-group-btn">
                <button type="submit" name="search" id="search-btn" class="btn btn-flat"><i class="fa fa-search"></i>
                </button>
              </span>
            </div>
          </form>
        </div>
        <div class="modal-body no-padding" style="overflow: auto; position:relative; height:460px;">
          <table class="table table-hover">
            <tbody v-if="actions && actions.length > 0">
            <tr v-for="(item, key) in actions" :key="key">
              <td style="padding-top:12px;">
              <i class="fa fa-circle-o" v-bind:style="{color: item.color}"></i>&nbsp;&nbsp;{{item.name}}
              <td>
                <div class="btn-group pull-right">
                  <button type="button" :class="selectedActionsIds.indexOf(item.id) > -1 ? 'btn btn-success' : 'btn btn-default'" @click="toggleCategory(item.id)"><i :class="selectedActionsIds.indexOf(item.id) > -1 ? 'fa fa-check' : 'fa fa-plus'"></i></button>
                </div>
              </td>
            </tr>
            </tbody>
            <tbody v-else>
              <tr>
                <td colspan="2">
                  No actions
                </td>
              </tr>
            </tbody>
          </table>
        </div>
        <div class="modal-footer">
          <button type="button" class="btn btn-default pull-left" data-toggle="modal" data-target="#modalCategoryCreate" @click="addCategory()"><i class="fa fa-plus-circle"></i> Create new</button>          
          <button type="button" class="btn btn-default" data-dismiss="modal" ref="dialogClose" @click="close">Close</button>
          <button type="button" class="btn btn-primary" @click="save">Save changes</button>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
  import { mapGetters } from 'vuex'
  export default {
    name: 'FormulaActions',
    computed: {
      ...mapGetters({
        formular: 'getFormula',
        actions: 'getFormulasActions',
        currentActionsIds: 'getFormulaActionsIds',
        modalType: 'getModalType',
        tabName: 'getActiveTabName',
        loading: 'getLoadingBar'
      })
    },
    data () {
      return {
        query: '',
        selectedActionsIds: [],
        selectedActions: [],
        result: '',
        action: {
          id: '',
          name: '',
          type: '',
          color: '#498a40',
          modified_by_name: '',
          modified_time: '',
          created_by_name: '',
          created_time: ''
        }
      }
    },
    created () {
    //   this.$store.dispatch('fetchFormulasActions', {query: this.query})
    },
    watch: {
      currentActionsIds: {
        handler () {
          this.selectedActionsIds = Object.assign([], this.currentActionsIds)
        }
      },
      modalType: {
        handler () {
          this.result = ''
          if (this.modalType === 5) this.selectedActionsIds = Object.assign([], this.currentActionsIds)
          if (this.modalType === 0 && this.tabName === 'Info') {
            this.result = 'success'
          }
        }
      }
    },
    methods: {
      toggleCategory (id) {
        let index = this.selectedActionsIds.indexOf(id)
        if (index > -1) {
          this.selectedActionsIds.splice(index, 1)
        } else {
          this.selectedActionsIds.push(id)
        }
      },
      save: function () {
        this.selectedCategories = []
        this.categories.map((item, ind) => {
          let index = this.selectedCategoriesIds.indexOf(item.id)
          if (index > -1) {
            this.selectedCategories.push(item)
          }
        })
        this.$store.dispatch('setFormulaCategories', this.selectedCategories)
        this.$store.dispatch('updateFormulaRelations', this.formular)
        this.result = 'success'
        const btn = this.$refs.dialogClose
        btn.click()
      },
      search: function (evt) {
        evt.preventDefault()
        this.$store.dispatch('setLoadingBar', 'block')
        this.$store.dispatch('setLoadingBarText', 'Searching...')
        this.$store.dispatch('fetchFormulasCategories', {query: this.query})
          .then((response) => {
            this.$store.dispatch('setLoadingBar', 'none')
          }).catch((error) => {
            console.log(error)
          })
      },
      addCategory: function () {
        this.category.name = ''
        this.category.type = 'formula'
        this.category.color = '#498a40'
        this.$store.dispatch('setModalType', 1)
        this.$store.dispatch('setCategoryValue', this.category)
      },
      editCategory: function (index) {
        let item = this.categories[index]
        this.category.name = item.name
        this.category.type = 'formula'
        this.category.color = item.color
        this.category.modified_time = item.modified_time
        this.category.modified_by_name = 'Modify By'
        this.category.created_time = item.created_time
        this.category.created_by_name = 'Create By'
        this.category.id = item.id
        this.$store.dispatch('setModalType', 2)
        this.$store.dispatch('setCategoryValue', this.category)
      },
      close: function () {
        if (this.result === 'success') {
          this.$store.dispatch('setModalType', 4)
        } else {
          this.$store.dispatch('setModalType', 6)
        }
      }
    }
  }
</script>
